#include<iostream>
using namespace std;

int main(){
	
	int a = 5;  //Datatype integer
	cout << a << endl;
	
	char ch = 'C';  // Datatype Character
	cout << ch << endl;
	
	bool bl = true; // Datatype boolean( True or False)
	cout << bl << endl;
	
	float f = 1.2;  // Datatype Float 
	cout<< f << endl;
	
	double d = 1.25; // Datatype Double 
	cout<< d << endl;
}

